/**
 * Get SEO data untuk website tertentu
 * @param {string} slug - Website slug
 * @returns {Object} - SEO data untuk website
 */
export function getWebsiteSEOData(slug) {
  return WEBSITE_SEO_DATA[slug] || WEBSITE_SEO_DATA['kelantan'];
}

/**
 * Get domain untuk website tertentu
 * @param {string} slug - Website slug
 * @returns {string} - Domain website
 */
export function getWebsiteDomain(slug) {
  const seoData = getWebsiteSEOData(slug);
  return seoData.domain;
}

/**
 * Generate structured data untuk website
 * @param {Object} website - Website data dari database
 * @returns {Object} - Website structured data
 */
export function generateWebsiteStructuredData(website) {
  const seoData = getWebsiteSEOData(website.slug);
  
  return {
    '@context': 'https://schema.org',
    '@type': 'WebSite',
    name: seoData.name,
    description: seoData.description,
    url: `https://${seoData.domain}`,
    potentialAction: {
      '@type': 'SearchAction',
      target: `https://${seoData.domain}/search?q={search_term_string}`,
      'query-input': 'required name=search_term_string'
    },
    publisher: {
      '@type': 'Organization',
      name: seoData.name,
      url: `https://${seoData.domain}`,
      sameAs: Object.values(seoData.socialMedia)
    },
    about: {
      '@type': 'Place',
      name: seoData.location,
      description: `Food and restaurant guide for ${seoData.location}`
    }
  };
}
